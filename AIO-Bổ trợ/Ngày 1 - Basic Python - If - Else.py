def calculate_can_chi_calendar(year):
    result = ''

    def calculate_can_chi(year):
        # Define the arrays for Can and Chi
        can = ["Canh", "Tân", "Nhâm", "Quý", "Giáp", "Ất", "Bính", "Đinh", "Mậu", "Kỷ"]
        chi = ["Thân", "Dậu", "Tuất", "Hợi", "Tý", "Sửu", "Dần", "Mão", "Thìn", "Tỵ", "Ngọ", "Mùi"]

        # Calculate the remainder for Can and Chi
        can_index = year % 10
        chi_index = year % 12

        # Get the Can and Chi from the arrays
        can_result = can[can_index]
        chi_result = chi[chi_index]

        return can_result, chi_result

    # Call calculate_can_chi to get Can and Chi
    can, chi = calculate_can_chi(year)
    # Format the result
    result = f"{can} {chi}"

    return result

