import numpy as np

# Tạo hai mảng dữ liệu
X = np.array([1, 2, 3, 4, 5])
Y = np.array([2, 4, 6, 8, 10])

# Tính toán hệ số tương quan
correlation_coefficient = np.corrcoef(X, Y)[0, 1]
print("Hệ số tương quan giữa X và Y là:", correlation_coefficient)

