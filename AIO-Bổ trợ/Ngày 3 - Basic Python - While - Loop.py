def find_squared_root(a):
    EPSILON = 0.001
    x_n = a  # Starting with x_n = a
    while True:
        x_next = (x_n + a / x_n) / 2
        if abs(x_next - x_n) < EPSILON:
            return x_next
        x_n = x_next

# Test cases
print("Test case 1: find_squared_root(2) ->", find_squared_root(2))
print("Test case 2: find_squared_root(3) ->", find_squared_root(3))